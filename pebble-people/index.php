          <h2>Validate</h2>
          <h4>Usability Testing</h4>
          <p>
            Once we were happy with the MVP version of the site we got it into the hands of user for testing. I ran usability testing sessions that unearthed some minor flaws in the system. We quickly remedied these and we were ready to go live
          </p>
          <br>
          <h4>Go Live</h4>
          <p>
            The go live plan centered around my user research plan. We selected a group of people and requested that they use the application at all stages of their Pebble People process. This would give us an insight into how the application faired at each stage of the process.
          </p>
          <p>
            The rest of the office was the control group. We notified everyone of the beta launch but wanted to see if people would adopt the application organically and at what stages of the process they would use it, rather than forcing it upon everyone!
          </p>
          <br>
          <h4>User Interviews</h4>
          <p>
            We waited for our selected group to be finished their Pebble People process, which involved self analysis and rating followed by a discussion with a manager.
          </p>
          <p>
            We then began our second round of user interviews. This project was a perfect opportunity to do some contextual inquiry. I sat with users at their own desks and watched them use the application, stopping them occasionally to ask questions.
          </p>
          <p>
            It was very insightful. For example a lot of the developers avoided the use of a mouse or trackpad at all costs! Some simple tweaks to the application to allow navigation solely with your keyboard was going to increase dramatically improve the user experience for them.
          </p>
